#!/usr/bin/perl
use strict;
use warnings;
use Getopt::Long;

#-----------------------------------------------------------------------------
#----------------------------------- MAIN ------------------------------------
#-----------------------------------------------------------------------------
my $usage = "

Synopsis:

cat config.json | fofn-gen.pl

Description:

No really, how the hell do you use this thing!

";



while (<STDIN>) {
    next if $_ =~ /{/;
    $_ =~ s/"|:|,//g;
    my @line = split /\s+/, $_;
    print $line[1], "\n";

}

exit 0;
